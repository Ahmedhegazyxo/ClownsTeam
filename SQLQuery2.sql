DROP DATABASE IF EXISTS Thriftly;
CREATE DATABASE Thriftly;
USE Thriftly;
CREATE TABLE Category
(
  Catid INT NOT NULL IDENTITY(1,1),
  CName VARCHAR(50) NOT NULL,
  PRIMARY KEY (Catid),
  UNIQUE (CName)
);

CREATE TABLE Governorate
(
  Gid INT NOT NULL IDENTITY(1,1),
  GName_en VARCHAR(50) NOT NULL,
  GName_ar VARCHAR(50) NOT NULL,
  PRIMARY KEY (Gid),
  UNIQUE (GName_en, GName_ar)
);

CREATE TABLE City
(
  CityId INT NOT NULL Identity,
  CityName_en VARCHAR(50) NOT NULL,
  CityName_ar VARCHAR(50) NOT NULL,
  Gid INT NOT NULL,
  PRIMARY KEY (CityId),
  FOREIGN KEY (Gid) REFERENCES Governorate(Gid),
  UNIQUE (CityName_en,CityName_ar,Gid)
);

CREATE TABLE Role
(
  Rid INT NOT NULL IDENTITY(1,1),
  RName VARCHAR(10) NOT NULL,
  PRIMARY KEY (Rid),
  UNIQUE (RName)
);

CREATE TABLE Account
(
  Password VARCHAR(50) NOT NULL,
  Email VARCHAR(50) NOT NULL,
  UserAdress VARCHAR(300) NOT NULL,
  Birthdate VARCHAR(20) NOT NULL,
  FirstName VARCHAR(20) NOT NULL,
  LastName VARCHAR(20) NOT NULL,
  Rating INT,
  PhoneNo CHAR(11) NOT NULL,
  Rid INT NOT NULL,
  PRIMARY KEY (Email),
  FOREIGN KEY (Rid) REFERENCES Role(Rid),
  UNIQUE (PhoneNo)
);

CREATE TABLE Product
(
  Pid INT NOT NULL  IDENTITY(1,1),
  PName VARCHAR(50) NOT NULL,
  Price FLOAT NOT NULL,
  Size VARCHAR(20),
  Material VARCHAR(20),
  NumberAvailable INT NOT NULL,
  Color VARCHAR(20),
  Description VARCHAR(200),
  Brand VARCHAR(20) NOT NULL,
  Email VARCHAR(50) NOT NULL,
  Catid INT NOT NULL,
  CityId INT NOT NULL,
  PRIMARY KEY (Pid),
  FOREIGN KEY (Email) REFERENCES Account(Email),
  FOREIGN KEY (Catid) REFERENCES Category(Catid),
  FOREIGN KEY (CityId) REFERENCES City(CityId)
);

